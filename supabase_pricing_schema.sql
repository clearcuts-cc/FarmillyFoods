-- Dynamic pricing schema for Farmmily.
-- Admin stores one base price per kg in `products`.
-- Variants store only weight and stock in `product_variants`.
-- Customer-side and admin-side both calculate final price dynamically.

create extension if not exists pgcrypto;

create table if not exists products (
  id bigint generated by default as identity primary key,
  name text not null,
  slug text unique,
  description text,
  image_url text,
  category_id bigint,
  base_price_per_kg numeric(10,2) not null check (base_price_per_kg >= 0),
  is_active boolean not null default true,
  is_featured boolean not null default false,
  rating numeric(3,1) not null default 5.0,
  review_count integer not null default 0,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

alter table products add column if not exists slug text;
alter table products add column if not exists description text;
alter table products add column if not exists image_url text;
alter table products add column if not exists category_id bigint;
alter table products add column if not exists is_active boolean not null default true;
alter table products add column if not exists is_featured boolean not null default false;
alter table products add column if not exists rating numeric(3,1) not null default 5.0;
alter table products add column if not exists review_count integer not null default 0;
alter table products add column if not exists created_at timestamptz not null default now();
alter table products add column if not exists updated_at timestamptz not null default now();

do $$
begin
  if exists (
    select 1
    from information_schema.columns
    where table_schema = 'public'
      and table_name = 'products'
      and column_name = 'base_price'
  ) and not exists (
    select 1
    from information_schema.columns
    where table_schema = 'public'
      and table_name = 'products'
      and column_name = 'base_price_per_kg'
  ) then
    alter table products rename column base_price to base_price_per_kg;
  end if;
end $$;

alter table products add column if not exists base_price_per_kg numeric(10,2);
update products set base_price_per_kg = 0 where base_price_per_kg is null;
alter table products alter column base_price_per_kg set default 0;
alter table products alter column base_price_per_kg set not null;

create table if not exists product_variants (
  id bigint generated by default as identity primary key,
  product_id bigint not null references products(id) on delete cascade,
  weight_kg numeric(10,2) not null check (weight_kg > 0),
  stock_count integer not null default 0 check (stock_count >= 0),
  is_active boolean not null default true,
  sort_order integer not null default 0,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  unique (product_id, weight_kg)
);

alter table product_variants add column if not exists stock_count integer not null default 0;
alter table product_variants add column if not exists is_active boolean not null default true;
alter table product_variants add column if not exists sort_order integer not null default 0;
alter table product_variants add column if not exists created_at timestamptz not null default now();
alter table product_variants add column if not exists updated_at timestamptz not null default now();

do $$
begin
  if exists (
    select 1
    from information_schema.columns
    where table_schema = 'public'
      and table_name = 'product_variants'
      and column_name = 'weight'
  ) and not exists (
    select 1
    from information_schema.columns
    where table_schema = 'public'
      and table_name = 'product_variants'
      and column_name = 'weight_kg'
  ) then
    alter table product_variants rename column weight to weight_kg;
  end if;
end $$;

alter table product_variants add column if not exists weight_kg numeric(10,2);
update product_variants set weight_kg = 0 where weight_kg is null;
alter table product_variants alter column weight_kg set default 0;
alter table product_variants alter column weight_kg set not null;

