create or replace function set_updated_at()
returns trigger
language plpgsql
as $$
begin
  new.updated_at = now();
  return new;
end;
$$;

drop trigger if exists trg_products_updated_at on products;
create trigger trg_products_updated_at
before update on products
for each row
execute function set_updated_at();

drop trigger if exists trg_product_variants_updated_at on product_variants;
create trigger trg_product_variants_updated_at
before update on product_variants
for each row
execute function set_updated_at();

create or replace view product_variant_prices as
select
  pv.id as variant_id,
  p.id as product_id,
  p.name as product_name,
  p.slug,
  p.description,
  p.image_url,
  p.category_id,
  p.base_price_per_kg,
  pv.weight_kg,
  concat(trim(to_char(pv.weight_kg, '999990.##')), ' kg') as weight_label,
  pv.stock_count,
  pv.is_active as variant_is_active,
  p.is_active as product_is_active,
  round(p.base_price_per_kg * pv.weight_kg, 2) as variant_price
from product_variants pv
join products p on p.id = pv.product_id;

comment on view product_variant_prices is
'Read-only view for storefront/admin lists. Variant price is calculated from products.base_price_per_kg * product_variants.weight_kg.';

